	public TextView getTvName() {
		return tvName;
	}

	public void setTvName(TextView tvName) {
		this.tvName = tvName;
	}

	public TextView getTvPhone() {
		return tvPhone;
	}

	public void setTvPhone(TextView tvPhone) {
		this.tvPhone = tvPhone;
	}

	public ImageView getIvTextBg() {
		return ivTextBg;
	}

	public void setIvTextBg(ImageView ivTextBg) {
		this.ivTextBg = ivTextBg;
	}

	public ImageView getIvSelectBg() {
		return ivSelectBg;
	}

	public void setIvSelectBg(ImageView ivSelectBg) {
		this.ivSelectBg = ivSelectBg;
	}
